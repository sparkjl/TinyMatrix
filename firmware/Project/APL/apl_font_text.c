static uint32_t font_cursor = 0;

Roll_DisplayTypeDef roll[16];

uint8_t apl_font_erase(uint8_t index)
{
  uint8_t  status = 1;
  uint16_t sector = 0;

  for(sector=(font_addr[index]/W25QX_SECTOR_SIZE); sector<(font_addr[index+1]/W25QX_SECTOR_SIZE); sector++)
  {
    status = bsp_w25qx_erase_sector(sector);
  }

  font_cursor = font_addr[index];

  return status;
}

void apl_font_upgrade(uint8_t *p_buff, uint16_t size)
{
  bsp_w25qx_write_sector(p_buff, font_cursor, size);
  font_cursor += size;
}


void apl_font_init(void)
{
  uint16_t num = sizeof(roll) / sizeof(Roll_DisplayTypeDef);

  for(uint16_t i=0; i<num; i++)
  {
    roll[i].x_dis = HUB75_PANEL_WIDTH - 1;
    roll[i].offset = 0;
    roll[i].p_dev = 0;
  }
}

void apl_font_display_char(uint16_t x, uint16_t y, uint8_t ch, uint8_t size, uint8_t mode)
{
  uint8_t i = 0, j = 0;
  uint8_t temp = 0;
  uint16_t y0 = y;
  uint8_t csize = (size/8+((size%8)?1:0))*(size/2);
  ch = ch - ' ';

  for(i=0; i<csize; i++)
  {
    if(size == 12)      temp = ascii_1206[ch][i];
    else if(size == 16) temp = ascii_1608[ch][i];
    else if(size == 24) temp = ascii_2412[ch][i];
    else
      return;

    for(j=0; j<8; j++)
    {
      if(temp & 0x80)
        apl_led_matrix_write_pixel(x, y, 1);
      else if(mode == 0)
        apl_led_matrix_write_pixel(x, y, 0);
      temp <<= 1;
      y++;
      //if(y >= HUB75_PANEL_HEIGHT) 
      //  return;
      if((y-y0) == size)
      {
        y = y0;
        x++;
        //if(x >= HUB75_PANEL_WIDTH)
        //  return;
        break;
      }
    }
  }
}

void apl_font_display_string(uint16_t x, uint16_t y, uint16_t width, uint16_t height, uint8_t *p_str, uint8_t size)
{
  uint16_t x0 = x;
  width  += x;
  height += y;

  while((*p_str >= ' ') && (*p_str <= '~'))
  {
    if(x >= width)
    {
      x = x0;
      y += size;
    }
    if(y >= height)
      break;

    apl_font_display_char(x, y, *p_str, size, 0);
    x += size/2;
    p_str++;
  }
}

void apl_font_gethz(uint8_t *p_hz, uint8_t *p_data, uint8_t size)
{
  uint8_t qh = 0, ql = 0;
  uint8_t i = 0;
  uint8_t csize = (size/8+((size%8)?1:0))*(size);
  uint32_t offset = 0;

  qh = *(p_hz++);
  ql = *(p_hz++);

  if(qh<0x81 || ql<0x40 || qh==0xff || ql==0xff)      // not a chinese font
  {
    for(i=0; i<csize; i++)
      *p_data++ = 0x00;      // fill with blank
      return;
  }

  qh -= 0x81;
  if(ql < 0x7f)
    ql -= 0x40;
  else
    ql -= 0x41;
  offset = (qh * 190 + ql) * (csize);

  switch(size)
  {
    case 12:
      bsp_w25qx_read(p_data, offset + font_addr[0], csize);
      break;
    case 16:
      bsp_w25qx_read(p_data, offset + font_addr[1], csize);
      break;
    case 24:
      bsp_w25qx_read(p_data, offset + font_addr[2], csize);
      break;
    default:
      break;
  }
}

void apl_font_display_hz(uint16_t x, uint16_t y, uint8_t *p_hz, uint8_t size, uint8_t mode)
{
  uint8_t i = 0, j = 0;
  uint8_t dzk[72];
  uint8_t temp = 0;
  uint16_t y0 = y;
  uint8_t csize = (size/8+((size%8)?1:0))*(size);

  if((size != 12) && (size != 16) && (size != 24))
    return;

  apl_font_gethz(p_hz, dzk, size);

  for(i=0; i<csize; i++)
  {
    temp = dzk[i];
    for(j=0; j<8; j++)
    {
      if(temp & 0x80)
        apl_led_matrix_write_pixel(x, y, 1);
      else if(mode == 0)
        apl_led_matrix_write_pixel(x, y, 0);
      temp <<= 1;
      y++;
      //if(y >= HUB75_PANEL_HEIGHT) 
      //  return;
      if((y-y0) == size)
      {
        y = y0;
        x++;
        //if(x >= HUB75_PANEL_WIDTH)
        //  return;
        break;
      }
    }
  }
}

void apl_font_display(uint16_t x, uint16_t y, uint16_t width, uint16_t height, uint8_t *p_str, uint8_t size, uint8_t mode)
{
  uint16_t x0 = x;
  uint16_t y0 = y;
  uint8_t is_hz = 0;            // is a chinese font?

  while(*p_str != 0)
  {
    if(is_hz == 0)
    {
      if(*p_str > 0x80)
        is_hz = 1;
      else            // english font
      {
        if(x > (x0 + width - (size/2)))
        {
          y += size;
          x = x0;
        }
        if(y > (y0 + height - size))      // out of bounds
          break;
        if(*p_str == '\r')      // 'ENTER' key
        {
          y += size;
          x = x0;
          p_str++;
        }
        else
          apl_font_display_char(x, y, *p_str, size, mode);
        p_str++;
        x += size/2;
      }
    }
    else            // chinese font
    {
      is_hz = 0;
      if(x > (x0 + width - size))
      {
        y += size;
        x = x0;
      }
      if(y > (y0 + height - size))
        break;
      apl_font_display_hz(x, y, p_str, size, mode);
      p_str += 2;
      x += size;
    }
  }
}

void apl_font_rolling(uint16_t y, uint8_t *p_str, uint8_t size, Roll_DisplayTypeDef *p_roll)
{
  uint16_t x = p_roll->x_dis;
  uint16_t offset = p_roll->offset;
  uint16_t p_dev = p_roll->p_dev;

  if((size != 12) && (size != 16) && (size != 24))
    return;

  apl_led_matrix_left_shift(y, size, 1);

  p_str += p_dev;
  if(*p_str == 0)
  {
    p_str -= p_dev;
    p_dev = 0;
  }

  if(*p_str > 0x80)            // is a chinese font?
  {
    apl_font_display_hz(x, y, p_str, size, 0);
    if(++offset == size)
    {
      x = HUB75_PANEL_WIDTH - 1;
      offset = 0;
      p_dev += 2;
    }
    else
      x--;
  }
  else
  {
    apl_font_display_char(x, y, *p_str, size, 0);
    if(++offset == size/2)
    {
      x = HUB75_PANEL_WIDTH - 1;
      offset = 0;
      p_dev++;
    }
    else
      x--;
  }

  p_roll->x_dis = x;
  p_roll->offset = offset;
  p_roll->p_dev = p_dev;
}

void apl_font_reverse(uint16_t x, uint16_t y, uint16_t width, uint16_t height)
{
  uint16_t x0 = x;
  uint16_t y0 = y;

  while(x < (x0 + width))
  {
    if(x >= HUB75_PANEL_WIDTH)
      break;

    y = y0;
    while(y < (y0 + height))
    {
      if(y >= HUB75_PANEL_HEIGHT)
        break;

      if(apl_led_matrix_read_pixel(x, y))
        apl_led_matrix_write_pixel(x, y, 0);
      else
        apl_led_matrix_write_pixel(x, y, 1);
      y++;
    }
    x++;
  }
}

